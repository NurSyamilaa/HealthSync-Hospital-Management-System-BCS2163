    private void btnTopsisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTopsisActionPerformed
        // TODO add your handling code here:
    java.util.List<TopsisRankingEngine.CandidateDoctor> candidates = new java.util.ArrayList<>();
    // Simulate real doctor metric evaluation parameters
    candidates.add(new TopsisRankingEngine.CandidateDoctor("Dr. Adam (Exp: 10yrs, Workload: 40hrs, Rating: 4.8)", 10, 40, 4.8));
    candidates.add(new TopsisRankingEngine.CandidateDoctor("Dr. Sarah (Exp: 2yrs, Workload: 15hrs, Rating: 4.2)", 2, 15, 4.2));
    candidates.add(new TopsisRankingEngine.CandidateDoctor("Dr. John (Exp: 8yrs, Workload: 48hrs, Rating: 4.9)", 8, 48, 4.9));

    // Run mathematical optimization calculation engine
    java.util.List<TopsisRankingEngine.CandidateDoctor> ranked = TopsisRankingEngine.rankDoctors(candidates);

    StringBuilder result = new StringBuilder("TOPSIS Prioritization Results for On-Call Shifts:\n\n");
    int rank = 1;
    for (TopsisRankingEngine.CandidateDoctor cd : ranked) {
        result.append(rank++).append(". ").append(cd.name)
              .append(" -> TOPSIS Score: ").append(String.format("%.4f", cd.topsisScore)).append("\n");
    }

    javax.swing.JOptionPane.showMessageDialog(this, result.toString(), "MCDM Priority Ranking Optimization", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnTopsisActionPerformed

