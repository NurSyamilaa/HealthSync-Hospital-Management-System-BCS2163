    private void refreshUiTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblDoctors.getModel();
        model.setRowCount(0); 
        
        for (Staff s : temporaryDoctorDb) {
            if (s instanceof Doctor) {
                Doctor d = (Doctor) s; 
                model.addRow(new Object[]{
                    d.getStaffID(),
                    d.getName(),
                    d.getEmail(),
                    d.getSpecialisation(),
                    d.getLicenseNumber()
                });
            }
        }
    }

    private void clearInputFields() {
        txtID.setText("");
        txtName.setText("");
        txtEmail.setText("");
        txtLicense.setText("");
        if (cmbSpecialisation.getItemCount() > 0) {
            cmbSpecialisation.setSelectedIndex(0);
        }
    }
